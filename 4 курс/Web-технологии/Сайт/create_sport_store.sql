PRAGMA foreign_keys = ON;

-- Удаление таблиц при повторном запуске скрипта (с учетом связей)
DROP TABLE IF EXISTS "ЭлементКорзины";
DROP TABLE IF EXISTS "СоставЗаказа";
DROP TABLE IF EXISTS "Корзина";
DROP TABLE IF EXISTS "Заказ";
DROP TABLE IF EXISTS "Товар";
DROP TABLE IF EXISTS "Категория";
DROP TABLE IF EXISTS "Покупатель";

------------------------------------------------------------
-- 1. Таблица Покупатель
------------------------------------------------------------
CREATE TABLE "Покупатель" (
    "ID_покупателя"     INTEGER PRIMARY KEY AUTOINCREMENT,
    "ФИО"               TEXT    NOT NULL,
    "Телефон"           TEXT    NOT NULL,
    "Email"             TEXT    NOT NULL,
    "Адрес_доставки"    TEXT    NOT NULL
);

------------------------------------------------------------
-- 2. Таблица Категория
------------------------------------------------------------
CREATE TABLE "Категория" (
    "ID_категории"           INTEGER PRIMARY KEY AUTOINCREMENT,
    "Наименование_категории" TEXT NOT NULL,
    "Описание_категории"     TEXT
);

------------------------------------------------------------
-- 3. Таблица Товар
------------------------------------------------------------
CREATE TABLE "Товар" (
    "ID_товара"             INTEGER PRIMARY KEY AUTOINCREMENT,
    "ID_категории"          INTEGER NOT NULL,
    "Наименование_товара"   TEXT    NOT NULL,
    "Цена"                  REAL    NOT NULL CHECK ("Цена" >= 0),
    "Краткое_описание"      TEXT,
    "Полное_описание"       TEXT,
    "Характеристики"        TEXT,
    "Изображение"           TEXT,
    "Наличие"               INTEGER NOT NULL DEFAULT 1 CHECK ("Наличие" IN (0, 1)),
    FOREIGN KEY ("ID_категории") REFERENCES "Категория" ("ID_категории") ON UPDATE CASCADE ON DELETE RESTRICT
);

------------------------------------------------------------
-- 4. Таблица Корзина
------------------------------------------------------------
CREATE TABLE "Корзина" (
    "ID_корзины"    INTEGER PRIMARY KEY AUTOINCREMENT,
    "ID_покупателя" INTEGER NOT NULL,
    "Дата_создания" TEXT    NOT NULL DEFAULT (datetime('now')),
    FOREIGN KEY ("ID_покупателя") REFERENCES "Покупатель" ("ID_покупателя") ON UPDATE CASCADE ON DELETE CASCADE
);

------------------------------------------------------------
-- 5. Таблица Заказ
------------------------------------------------------------
CREATE TABLE "Заказ" (
    "ID_заказа"      INTEGER PRIMARY KEY AUTOINCREMENT,
    "ID_покупателя"  INTEGER NOT NULL,
    "Дата_заказа"    TEXT    NOT NULL DEFAULT (datetime('now')),
    "Статус_заказа"  TEXT    NOT NULL DEFAULT 'Новый',
    "Итоговая_сумма" REAL    NOT NULL DEFAULT 0 CHECK ("Итоговая_сумма" >= 0),
    "Комментарий"    TEXT,
    FOREIGN KEY ("ID_покупателя") REFERENCES "Покупатель" ("ID_покупателя") ON UPDATE CASCADE ON DELETE CASCADE
);

------------------------------------------------------------
-- 6. Таблица СоставЗаказа
------------------------------------------------------------
CREATE TABLE "СоставЗаказа" (
    "ID_состава_заказа"      INTEGER PRIMARY KEY AUTOINCREMENT,
    "ID_товара"              INTEGER NOT NULL,
    "ID_заказа"              INTEGER NOT NULL,
    "Количество"             INTEGER NOT NULL CHECK ("Количество" > 0),
    "Цена_на_момент_заказа"  REAL    NOT NULL CHECK ("Цена_на_момент_заказа" >= 0),
    FOREIGN KEY ("ID_товара") REFERENCES "Товар" ("ID_товара") ON UPDATE CASCADE ON DELETE RESTRICT,
    FOREIGN KEY ("ID_заказа") REFERENCES "Заказ" ("ID_заказа") ON UPDATE CASCADE ON DELETE CASCADE
);

------------------------------------------------------------
-- 7. Таблица ЭлементКорзины
------------------------------------------------------------
CREATE TABLE "ЭлементКорзины" (
    "ID_элемента_корзины" INTEGER PRIMARY KEY AUTOINCREMENT,
    "ID_товара"           INTEGER NOT NULL,
    "ID_корзины"          INTEGER NOT NULL,
    "Количество"          INTEGER NOT NULL CHECK ("Количество" > 0),
    FOREIGN KEY ("ID_товара")  REFERENCES "Товар"   ("ID_товара")   ON UPDATE CASCADE ON DELETE RESTRICT,
    FOREIGN KEY ("ID_корзины") REFERENCES "Корзина" ("ID_корзины") ON UPDATE CASCADE ON DELETE CASCADE
);

